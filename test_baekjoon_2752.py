def three_n_s(s):
    ls = s.split()
    for t in range(3):
        ls[t] = int(ls[t])
    for i in range(2):
        for k in range(2):
            if ls[k] > ls[k+1]:
                x = ls[k]
                y = ls[k+1]
                ls[k+1] = x
                ls[k] = y
    result = ''
    for j in range(3):
        result += str(ls[j]) + ' '
    return result
def test_three_n_s():
    assert three_n_s('3 1 2') == '1 2 3 '