def union(nums,inputs):
    n1, n2 = map(int, nums.split())
    ls = []
    for n, i in zip(range(2),[n1,n2]):
        var = map(int, inputs[n].split())
        for k in var:
            ls.append(k)
    ls.sort()
    result = ''
    for j in range(len(ls)):
        if j == (len(ls)-1):
            result += ls[j]
        else:
            result += ls[j] + ' '
    return result
def test_union():
    assert union('2 1', ['4 7','1']) == '1 4 7'